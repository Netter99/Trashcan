package org.entity;

public class Credit {
	String openid;
	int credit;
	
	public Credit() {
		this.credit = credit;
	}

	public Credit(int credit) {
		this.credit = credit;
	}
	public String getopenid() {
		return openid;
	}
	public int getCredit() {
		return credit;
	}
	public void setCredit(int credit) {
		this.credit = credit;
	}
}
